func TestWriteValuesStub_HappyPath(t *testing.T) {
	t.Parallel()

	fsys := vfs.NewMemMapFS()
	dir := "/unit"
	require.NoError(t, fsys.MkdirAll(dir, 0o755))

	refs := redesign.ValuesReferences{
		Required: []string{"region", "app"},
		Optional: []redesign.OptionalValue{
			{Name: "aws_partition", Default: cty.StringVal("aws")},
			{Name: "deploy_branch", Default: cty.StringVal("main")},
		},
	}

	written, err := redesign.WriteValuesStub(fsys, dir, refs)
	require.NoError(t, err)
	assert.True(t, written)

	raw, err := vfs.ReadFile(fsys, filepath.Join(dir, "terragrunt.values.hcl"))
	require.NoError(t, err)

	out := string(raw)

	assert.Contains(t, out, "# Auto-generated by `terragrunt catalog`")
	assert.Contains(t, out, "# === Required ===")
	assert.Contains(t, out, "# === Optional (defaults from try() fallbacks) ===")

	// Required block comes before optional block.
	requiredIdx := strings.Index(out, "# === Required ===")

	optionalIdx := strings.Index(out, "# === Optional")

	assert.Less(t, requiredIdx, optionalIdx)

	// Within Required: app < region.
	appIdx := strings.Index(out, "\napp")

	regionIdx := strings.Index(out, "\nregion")

	require.NotEqual(t, -1, appIdx)
	require.NotEqual(t, -1, regionIdx)
	assert.Less(t, appIdx, regionIdx)

	// Required entries are TODO placeholders.
	assert.Regexp(t, `app\s*=\s*"TODO"`, out)
	assert.Regexp(t, `region\s*=\s*"TODO"`, out)

	// Optional entries use the evaluated defaults.
	assert.Regexp(t, `aws_partition\s*=\s*"aws"`, out)
	assert.Regexp(t, `deploy_branch\s*=\s*"main"`, out)
}

func TestWriteValuesStub_EmptyIsNoOp(t *testing.T) {
	t.Parallel()

	fsys := vfs.NewMemMapFS()
	dir := "/unit"
	require.NoError(t, fsys.MkdirAll(dir, 0o755))

	written, err := redesign.WriteValuesStub(fsys, dir, redesign.ValuesReferences{})
	require.NoError(t, err)
	assert.False(t, written)

	exists, err := vfs.FileExists(fsys, filepath.Join(dir, "terragrunt.values.hcl"))
	require.NoError(t, err)
	assert.False(t, exists)
}

func TestWriteValuesStub_SkipsWhenExists(t *testing.T) {
	t.Parallel()

	fsys := vfs.NewMemMapFS()
	dir := "/unit"
	path := filepath.Join(dir, "terragrunt.values.hcl")

	original := []byte("# user-authored content\nfoo = \"existing\"\n")
	require.NoError(t, vfs.WriteFile(fsys, path, original, 0o644))

	refs := redesign.ValuesReferences{Required: []string{"foo", "bar"}}

	written, err := redesign.WriteValuesStub(fsys, dir, refs)
	require.NoError(t, err)
	assert.False(t, written)

	got, err := vfs.ReadFile(fsys, path)
	require.NoError(t, err)
	assert.Equal(t, original, got, "existing values file should be untouched")
}
