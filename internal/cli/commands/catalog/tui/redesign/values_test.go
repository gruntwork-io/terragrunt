// optionalNames returns the names from refs.Optional in their current order.
// Use in tests that want to assert on presence + ordering of optional entries
// without caring about the concrete defaults.
func optionalNames(refs redesign.ValuesReferences) []string {
	out := make([]string, 0, len(refs.Optional))
	for _, o := range refs.Optional {
		out = append(out, o.Name)
	}

	return out
}

func TestCollectValuesReferences_Unit(t *testing.T) {
	t.Parallel()

	fsys := vfs.NewMemMapFS()
	path := "/unit/terragrunt.hcl"

	body := `
locals {
  region = values.region
  app    = values.app
}

inputs = {
  region = values.region
  extra  = "${values.env}-literal"
}
`
	require.NoError(t, vfs.WriteFile(fsys, path, []byte(body), 0o644))

	refs, err := redesign.CollectValuesReferences(fsys, path)
	require.NoError(t, err)
	assert.Equal(t, []string{"app", "env", "region"}, refs.Required)
	assert.Empty(t, refs.Optional)
}

func TestCollectValuesReferences_SplitsRequiredAndOptional(t *testing.T) {
	t.Parallel()

	fsys := vfs.NewMemMapFS()
	path := "/unit/terragrunt.hcl"

	// Required: region, app (app also appears in a try but the required
	// usage wins).
	// optional (literal default): aws_partition, deploy_branch, tags, aud.
	// optional (non-literal default, skipped): issuer (local ref).
	body := `
locals {
  region         = values.region
  app_required   = values.app
  app_fallback   = try(values.app, "default")
  aws_partition  = try(values.aws_partition, "aws")
  deploy_branch  = try(values.deploy_branch, "main")
  aud            = try(values.aud, "sts.amazonaws.com")
  tags           = try(values.tags, {})
  issuer         = try(values.issuer, local.default_issuer)
  interpolated   = try(values.interp, "${local.x}-suffix")
}
`
	require.NoError(t, vfs.WriteFile(fsys, path, []byte(body), 0o644))

	refs, err := redesign.CollectValuesReferences(fsys, path)
	require.NoError(t, err)
	assert.Equal(t, []string{"app", "region"}, refs.Required)
	assert.Equal(t, []string{"aud", "aws_partition", "deploy_branch", "tags"}, optionalNames(refs))
}

